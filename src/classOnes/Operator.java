
package classOnes;

public class Operator {
    
    //ATTRIBUTES
    public double result;
    
    //METHODS
    //Making the operators
    public void add(double n1, double n2 ) { result = n1 + n2;}
    public void sub(double n1, double n2 ) { result = n1 - n2;}
    public void mul(double n1, double n2 ) { result = n1 * n2;}
    public void div(double n1, double n2 ) { result = n1 / n2;}
    public void pow(double n1, double n2 ) { result = Math.pow(n1,n2);}
    
}
